package lesson5;

public class operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(b--);
    }
}
