package lesson16;

import java.util.Deque;
import java.util.LinkedList;

public class LinkedListApp3 {

    public static void main(String[] args) {
        Deque<String> words = new LinkedList<>();
        words.add("NEVHERNVERVJ");
        words.add("FHEFHEIJIWJI");
        words.add("HEUYGDWUEJEWI");
        words.add("DBUEGYFWFEUFHEF");
        words.peek();
    }

}
